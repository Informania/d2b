<p class="textLeft">Till att börja med så vill vi etablera oss som en säker och pålitlig leverantör av plastdistanser. Vi kommer även att ha växande möjligheter att leverera andra produkter inom detta område. <br />
Vårt långsiktiga mål är att bli Sveriges ledande producent och leverantör av plastdistanser, och vi kommer sträva efter att vara den mest stabila såväl som och flexibla leverantören på marknaden.</p>

